ll combination(ll n, ll k) {
    ll r = 1;
    for (ll d = 1; d <= k; ++d) {
        r *= n--;
        r /= d;
    }
    return r;
}

void add_ab(string &S, ll left_a, ll left_b, ll K, ll max_size){
    if(S.size() == max_size) return;
    ll comb_select_a = combination(left_a-1 + left_b, left_b);
    if(K <= comb_select_a){
        S.push_back('a');
        left_a--;
        add_ab(S, left_a, left_b, K, max_size);
    }
    else{
        S.push_back('b');
        left_b--;
        K -= comb_select_a;
        add_ab(S, left_a, left_b, K, max_size);
    }
}

